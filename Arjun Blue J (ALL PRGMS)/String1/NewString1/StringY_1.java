package String1.NewString1;

public class StringY_1
{
    public static void main(String s)
    {
        for(int i= 0;i<s.length();i++)
        {
            for(int j=0;j<=i;j++)
            {
                System.out.print(s.charAt(j));
            }
            System.out.println();
        }
    }
}
